# Student Information using Strings

college = "Sreenidhi University"
student_name = "Sahasra"
course = "Python Programming"

print("College Name:", college)
print("Student Name:", student_name)
print("Course:", course)

# String operations
print("\nUpper Case:", student_name.upper())
print("Lower Case:", course.lower())
print("Length of College Name:", len(college))

# First and last character
print("First Letter:", student_name[0])
print("Last Letter:", student_name[-1])

# String Concatenation
message = student_name + " is learning " + course
print(message)